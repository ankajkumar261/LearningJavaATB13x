package Coding_programs_test;

public class diff_posin_prein {
    public static void main(String[] args) {

        int a = 1;
        System.out.println(++a);
//        pre increment which means 1 + 1 = 2

        int b = 2;
        System.out.println(b++);
//        post increment mean add will come after
        System.out.println(b);
    }
}
